  blogsByLocale[locale] || blogs;

export const getBlogBySlug = (slug, locale = "en") => {
  const list = getBlogsForLocale(locale);
  return list.find(
    (blog) =>
      String(blog.slug).toLowerCase() === String(slug).toLowerCase(),
  );
};
